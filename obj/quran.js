var sorToInd = indToSor.reduce((obj, sor, i) => { obj[sor] = i; return obj }, {});

var pageToSor = [];
for (let sor in sormap) {
	let [start, end] = sormap[sor];
	for (let i = 0, e = end - start +1; i < e; i++) pageToSor[i + start] = sor
}

var sorFahras = {};
for (let sor in sormap) {
	let [start, end] = sormap[sor], sorInd = sorToInd[sor];
	sorFahras[sor] = Object.fromEntries(Array.from(
		{length: end - start +1}, 
		(v, i) => [`صفحة (${i + start})`, `${sorInd}/${i + start}`])
	)
}

var pageFahras = {};
for (let h = 0; h < 6; h++) {
	let hObj = {};
	for (let t = 0; t < 10; t++) {
		let tObj = {};
		for (let o = 0; o < 10; o++) tObj[`${h}${t}${o}`] = `${sorToInd[pageToSor[h * 100 + t * 10 + o]]}/${h}${t}${o}`;
		hObj[`${h}${t}0 - ${h}${t}9`] = tObj;
	}
	pageFahras[`${h}00 - ${h}99`] = hObj
}
pageFahras['600 - 604'] = {
	'600': '100/600',
	'601': '103/601',
	'602': '106/602',
	'603': '108/603',
	'604': '112/604',
}
delete pageFahras['000 - 099']['000 - 009']['000']; //no exist for 0 page in quran

